
let i = 10;

while (i >= 0) {

    if (i === 10) {
        console.log("Commencing countdown");
    } else if (i === 0) {
        console.log ("Blastoff");
    } else {
        console.log(`${i} seconds to blastoff !`)
    };

    i-- ;

};




